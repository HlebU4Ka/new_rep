def to_uppercase(string):
    """
    Функция, которая принимает на вход строку и возвращает ее со всеми заглавными буквами
    """
    return string.upper()


def capitalize_words(string):
    """
    Принимает на вход строку и возвращает новую строку, в которой первые буквы каждого слова заглавные.

    Аргументы:
    string -- строка, которую нужно изменить

    Возвращаемое значение:
    Новая строка, в которой первые буквы каждого слова заглавные.
    """
    return string.title()